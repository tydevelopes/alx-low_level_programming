#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: number 1
 * @b: number 2
 * Return: results
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract numbers
 * @a: number 1
 * @b: number 2
 * Return: results
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply numbers
 * @a: number 1
 * @b: number 2
 * Return: results
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide numbers
 * @a: number 1
 * @b: number 2
 * Return: results
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc modulus
 * @a: number 1
 * @b: number 2
 * Return: results
 */
int op_mod(int a, int b)
{
	return (a % b);
}
